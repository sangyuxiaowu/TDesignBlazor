namespace TDesign;
/// <summary>
/// 表示状态的候选值。
/// </summary>
public enum Status
{
    /// <summary>
    /// 默认状态。
    /// </summary>
    Default,
    /// <summary>
    /// 成功的状态。
    /// </summary>
    Success,
    /// <summary>
    /// 错误的状态。
    /// </summary>
    Error,
    /// <summary>
    /// 警告的状态。
    /// </summary>
    Warning
}